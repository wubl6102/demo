package com.vo;

public class Score {
	private String score;
	private String cour_name;
	private String cour_mark;
	
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getCour_name() {
		return cour_name;
	}
	public void setCour_name(String cour_name) {
		this.cour_name = cour_name;
	}
	public String getCour_mark() {
		return cour_mark;
	}
	public void setCour_mark(String cour_mark) {
		this.cour_mark = cour_mark;
	}
	
}
